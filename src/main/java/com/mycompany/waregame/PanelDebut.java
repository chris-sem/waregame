    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        if(nomj1.getText().equals("") || nomj2.getText().equals("")){
            message.setText("Vous dreviez entrer les noms des deux joueurs");
        }
        else{
            
            Waregame.tableauDesauvegarde = null ;
            
            Waregame.id_joueur_courant = 1 ;
            Waregame.nom_joueur1 = nomj1.getText();
            Waregame.nom_joueur2 = nomj2.getText();
            Waregame.num_tour_joueur1 = Waregame.nombreMaxTourAjouer ;
            Waregame.num_tour_joueur2 = Waregame.nombreMaxTourAjouer ;
            
            FenetreJeu.remplirToutesLesCellules(FenetreJeu.getPlacementUniteInitial());
            
            Waregame.fj.PanelEntete.setLayout(new BorderLayout());
            Waregame.fj.PanelEntete.removeAll();
            PanelJeuEnCours jp_gc = new PanelJeuEnCours();
            Waregame.fj.PanelEntete.add(jp_gc);
            Waregame.fj.PanelEntete.revalidate();
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

